    def MeasureFlow_Failover_PerHop(self, TrialNumber, TestDuration):
        WaveEngine.WriteDetailedLog(['Flow Name', 'src_port', 'src_client', 'des_port', 'des_client', 'txFlowFramesOk', 'txFlowOctetsOk', 'rxFlowFramesOk', 'rxFlowOctetsOk', 'OutOfSequence', 'Loss'])
        EthernetPorts = []
        for eachClient in self.EthernetClients:
            if eachClient[1] not in EthernetPorts:
                EthernetPorts.append(eachClient[1])

        AverageTotalTime = 0.0
        AverageCount     = 0
        HopIndex = 0
        for eachHop in self.HopFlowList:
            if len(eachHop)> 0:
                TotalTXframesUP   = 0
                TotalRXframesUP   = 0
                TotalTXframesDOWN = 0
                TotalRXframesDOWN = 0
                for Flowname in eachHop.keys():
                    (src_port, src_client, des_port, des_client) = eachHop[Flowname]
                    WaveEngine.VCLtest("flowStats.read('%s','%s')" % (src_port, Flowname))
                    TXframes = flowStats.txFlowFramesOk
                    TXoctets = flowStats.txFlowOctetsOk
                    WaveEngine.VCLtest("flowStats.read('%s','%s')" % (des_port, Flowname))
                    WaveEngine.WriteDetailedLog([Flowname, src_port, src_client, des_port, des_client, TXframes, TXoctets, flowStats.rxFlowFramesOk, flowStats.rxFlowOctetsOk, flowStats.rxFlowOutOfSequenceFrames ,TXframes - flowStats.rxFlowFramesOk])
                    if des_port in EthernetPorts:
                        TotalTXframesUP   += TXframes
                        TotalRXframesUP   += flowStats.rxFlowFramesOk
                    else:
                        TotalTXframesDOWN += TXframes
                        TotalRXframesDOWN += flowStats.rxFlowFramesOk
                        
                #Compute metrics
                if TotalTXframesUP > 0:
                    ActualTXrate = float(TotalTXframesUP) / float(TestDuration)
                    RecoveryTime = (TotalTXframesUP - TotalRXframesUP) / ActualTXrate
                    self.ResultsForCSVfile.append( (TrialNumber, HopIndex, 'Up', TotalTXframesUP, TotalRXframesUP, RecoveryTime, 1.0/ActualTXrate) )
                    AverageTotalTime += RecoveryTime
                    AverageCount     += 1
                    
                if TotalTXframesDOWN > 0:
                    ActualTXrate = float(TotalTXframesDOWN) / float(TestDuration)
                    RecoveryTime = (TotalTXframesDOWN - TotalRXframesDOWN) / ActualTXrate
                    self.ResultsForCSVfile.append( (TrialNumber, HopIndex, 'Down', TotalTXframesDOWN, TotalRXframesDOWN, RecoveryTime, 1.0/ActualTXrate) )
                    AverageTotalTime += RecoveryTime
                    AverageCount     += 1

                if TotalTXframesUP + TotalTXframesDOWN == 0:
                    WaveEngine.OutputstreamHDL("\nWarning: No frames were transmitted; Recovery Time is invalid.\n", MSG_WARNING)
                if TotalRXframesUP + TotalRXframesDOWN == 0:
                    WaveEngine.OutputstreamHDL("\nWarning: No frames were receievd; SUT/DUT never recovered.\n", MSG_WARNING)
                
            HopIndex += 1
        return AverageTotalTime/float(AverageCount)

    def startTest(self, FuncRealTime):
        self.ResultsForCSVfile.append( ('Trial', 'Hop', 'Direction', 'TX frames', 'RX frames', 'Recovery Time', 'Accuracy') )
        ActiveGroup = "XmitGroup"
        FrameSize = int(self.FrameSize)
        FrameRate = float(self.ILOAD)
        FrameRatePerFlow = FrameRate / float(len(self.FlowList))
        NumFrames = int(FrameRatePerFlow * self.TransmitTime)
        PassedParam = {'Title': None, 'CardList': self.CardList, 'Type': self.FlowOptions['Type']}
        #Count the active hops
        ActiveHops = 0
        for eachhHop in self.WirelessClients:
            if len(eachhHop) > 0:
                ActiveHops += 1

        for TrialNumber in range(1, 1 + self.Trials):
            #Learning Phase
            if self.FlowLearningTime > 0.0:
                PassedParam['Title'] = "Training DUT/SUT:"
                PassedParam['BitsPerFrame'] = 512
                self._transmitIteration(self.FlowLearningTime, 0, self.UpdateInterval, ActiveGroup, True, FuncRealTime, PassedParam)
                WaveEngine.OutputstreamHDL("\n", WaveEngine.MSG_OK)

            #Clear the counters
            WaveEngine.ClearAllCounter(self.CardList)
            WaveEngine.WriteAPinformation(self.ListOfClients)
            WaveEngine.ModifyFlows(self.FlowList, {'FrameSize': FrameSize, 'IntendedRate': FrameRatePerFlow , 'NumFrames': NumFrames, 'RateMode': 'pps'})
            PassedParam['Title'] = " " 


            #Wait for user input before continueing
            #FIXME - make this a trigger to an extrenal event.
            WaveEngine.OutputstreamHDL("Press anything to start Trial %d" % (TrialNumber), WaveEngine.MSG_QUERY)

            #Run the meaurement.
            WaveEngine.OutputstreamHDL("Trial:%d FrameSize: %s ILOAD:%s pkts/sec, Starting iteration\n" % (TrialNumber, FrameSize, FrameRate), WaveEngine.MSG_OK)
            self.TransmitIteration(self.TransmitTime, self.SettleTime, self.UpdateInterval, ActiveGroup, True, FuncRealTime, PassedParam)
            WaveEngine.OutputstreamHDL("\n", WaveEngine.MSG_OK)

            #Get the counts
            WaveEngine.WriteDetailedLog([''])
            WaveEngine.WriteDetailedLog(['Trial:', TrialNumber, 'Active Hops:', ActiveHops])
            AverageFailOver = self.MeasureFlow_Failover_PerHop(TrialNumber, self.TransmitTime)

            #Done
            #FIXME - add a number to the messag ebelow
            WaveEngine.OutputstreamHDL("Completed: Trial %d average Failover Time is %ss\n" %(TrialNumber, Float2EngNotation(AverageFailOver, self.DisplayPrecision)), WaveEngine.MSG_SUCCESS)
                
            # Certain DUT do not like the constant traffic
            if self.AgingTime > 0:
                WaveEngine.Sleep(self.AgingTime, 'DUT/SUT recovery time,')

    def ParseCVSdata(self, dataFromFile):
        # Data for the Graph
        self._ReportData   = {}
        # List of Hops active in the graph
        self._ReportHops   = []
        # Data for the detailed section of the Report
        self._ReportResults = [('Trial', 'Hop', 'Direction', 'TX frames', 'RX frames', 'Recovery Time', 'Accuracy'),]

        for eachLine in dataFromFile:
            #print len(eachLine), ':', eachLine
            if len(eachLine) != 7:
                continue
            
            (TrialNumber, Hop, Direction, TXframe, RXframe, RecoveryTime, Accuracy) = eachLine
            if not isnum(TrialNumber):
                #Process Titles
                continue
            
            Hop = int(Hop)
            if not Hop in self._ReportHops:
                self._ReportHops.append(Hop)
            
            if self._ReportData.has_key(Hop):
                (TotalMin, TotalMax, TotalAvg, TotatAccuracy, Count) = self._ReportData[Hop]
                if TotalMin > RecoveryTime:
                    TotalMin = RecoveryTime
                if RecoveryTime > TotalMax:
                    TotalMax = RecoveryTime
                self._ReportData[Hop] = (TotalMin, TotalMax, TotalAvg + RecoveryTime, TotatAccuracy + Accuracy, Count + 1)
            else:
                self._ReportData[Hop] = (RecoveryTime, RecoveryTime, RecoveryTime, Accuracy, 1)

