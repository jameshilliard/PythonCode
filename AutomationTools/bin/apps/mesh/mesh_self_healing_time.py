    def configureFlows(self):
        # Set the flows up initally with the learning paramters
        self.FlowOptions['NumFrames']    = int( self.FlowLearningTime * self.FlowLearningRate )
        self.FlowOptions['IntendedRate'] = self.FlowLearningRate
        self.FlowOptions['RateMode']     = 'pps'

        #Set the direction flag
        if self.FlowDirection == FlowPattern.both:
            self.BiDirectional = True
        else:
            self.BiDirectional = False

        #Create a new flow map one Eth to manty WiFi
        MaximumHops = len(self.WirelessClients)
        self.HopFlowList = []
        if self.FlowDirection == FlowPattern.up:
            for hop in range(MaximumHops):
                if len(self.WirelessClients[hop]) > 0:
                    SrcClients = OrderedDict()
                    for eachClient in self.WirelessClients[hop]:
                        BaseLength = len(eachClient[0])
                        for eachKey in self.ListofSrcClient.keys():
                            if eachClient[0] == eachKey[:BaseLength]:
                                SrcClients[eachKey] = self.ListofSrcClient[eachKey]
                    self.HopFlowList.append(self.FlowMap(SrcClients, self.ListofDesClient, self.BiDirectional, self.FlowOptions) )
                else:
                    self.HopFlowList.append( OrderedDict() )
        else:
            for hop in range(MaximumHops):
                if len(self.WirelessClients[hop]) > 0:
                    DesClients = OrderedDict()
                    for eachClient in self.WirelessClients[hop]:
                        BaseLength = len(eachClient[0])
                        for eachKey in self.ListofDesClient.keys():
                            if eachClient[0] == eachKey[:BaseLength]:
                                DesClients[eachKey] = self.ListofDesClient[eachKey]
                    self.HopFlowList.append(self.FlowMap(self.ListofSrcClient, DesClients, self.BiDirectional, self.FlowOptions) )
                else:
                    self.HopFlowList.append( OrderedDict() )

        #Put the indivisal hop flows into one list
        self.FlowList = OrderedDict()
        for hop in range(MaximumHops):
            if len(self.HopFlowList[hop]) > 0:
                dict = self.HopFlowList[hop]
                for eachKey in dict.keys():
                    self.FlowList.update(dict)
                
        self._createFlowGroup(self.FlowList, "XmitGroup")
        self.TotalFlows = len(self.FlowList)

