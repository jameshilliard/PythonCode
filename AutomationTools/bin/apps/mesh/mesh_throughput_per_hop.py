    def startTest(self, FuncRealTime):
        if self.UserPassFailCriteria['user']=='True':
                self.ResultsForCSVfile.append( ('Frame Size', 'Trial', 'Node ID', 'Hop', 'Theoretical Throughput pkts/sec', 'Theoretical Throughput bits/sec', 'ILOAD pkts/sec', 'Throughput pkts/sec', 'Throughput bits/sec','USC:AT') )
