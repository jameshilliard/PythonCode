=head1 NAME
verifyPing.pl is used to try to ping an ip and see whether it's able to ping thru

=head1 SYNOPSIS

=over

=item B<verifyPing.pl>
[B<-help|-h>]
[B<-man>]
[B<-o> I<output file to save file >]
[B<-l> I<log file directory>]
[B<-d> I<target address>]
[B<-u> I<remote user name>]
[B<-p> I<password>]
[B<-src> I<source file,full path>]
[B<-dst> I<destination path>]
[B<-s> I<send to remote>]
[B<-t> I<time out for each command executed by verifyPing.pl >]


=back

=head1 OPTIONS AND ARGUMENTS

=over

=item B<-help>

Print a brief help message and exit.

=item B<-man>

Print a man page and exit.

=item B<-o>

Output file where the output of ping will be stored

=item B<-l >

Redirect stdout to the /path/verifyPing.log

=item B<-d >

Target ip address

=item B<-u>

Remote PC user name

=item B<-p>

Remote PC password

=item B<-src>

Source file,with full path

=item B<-dst>

Destination path or file name after scp

=item B<-s>

Send file to target PC,if without this option,default operation is scp file from remote to local.

=item B<-t >

Set timeout in seconds for scp operation ( default = 60 seconds)

=item B<-x>

Set debug to different level . ( more debug messages with higher number)

=back

=head1 EXAMPLES

=over

1. The following command is used to copy a file named source_file from 192.168.100.1:/root/actiontec/ to local directory /tmp

	 perl verifyPing.pl -d 192.168.100.1 -u root -p actiontec -src /root/automation/source_file  -dst /temp -l /tmp/logs/ -o scpFile_test.log

2. The following command is used to copy a file named source_file from local directory /tmp to 192.168.100.1:/root/actiontec/

	 perl verifyPing.pl -d 192.168.100.1 -u root -p actiontec -src /tmp/source_file -dst /root/automation -l /tmp/logs/ -s -o scpFile_test.log

=back

